-- Financial Library Database Schema
-- 국내 금융 정보 웹 도서관 데이터베이스

CREATE DATABASE IF NOT EXISTS financial_library
CHARACTER SET utf8mb4
COLLATE utf8mb4_unicode_ci;
USE financial_library;


-- 금융 정보 카테고리 테이블
CREATE TABLE categories (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 금융 정보 테이블
CREATE TABLE financial_documents (
    id INT AUTO_INCREMENT PRIMARY KEY,
    title VARCHAR(500) NOT NULL,
    content TEXT NOT NULL,
    summary TEXT,
    category_id INT,
    author VARCHAR(200),
    source VARCHAR(200),
    publication_date DATE,
    tags TEXT,
    view_count INT DEFAULT 0,
    is_featured BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (category_id) REFERENCES categories(id) ON DELETE SET NULL
);


-- 인덱스 생성
CREATE INDEX idx_financial_documents_category ON financial_documents(category_id);
CREATE INDEX idx_financial_documents_title ON financial_documents(title);
CREATE INDEX idx_financial_documents_created_at ON financial_documents(created_at);

